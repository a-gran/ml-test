# ─────────────────────────────────────────────────────────────────────────────
# ROC-кривая (Receiver Operating Characteristic) — это график, который показывает,
# насколько хорошо модель РАЗДЕЛЯЕТ два класса при разных порогах.
# Ось X: False Positive Rate (FPR) — доля здоровых, ошибочно названных больными.
# Ось Y: True Positive Rate (TPR) — доля больных, правильно найденных моделью.
# Идеальная модель: кривая идёт в левый верхний угол (TPR=1, FPR=0).
# Случайная модель: диагональ (пунктирная линия).
# Чем больше площадь под кривой (AUC) — тем лучше модель.
fig, ax = plt.subplots(figsize=(7, 6))  # Создаём отдельную фигуру для ROC.

# roc_curve() — вычисляет точки ROC-кривой.
# Возвращает: fpr (ось X), tpr (ось Y), пороги (нам не нужны, поэтому _ ).
fpr, tpr, _ = roc_curve(y_test, y_pred_proba)

# Рисуем ROC-кривую нашей модели.
ax.plot(fpr, tpr, color='#e74c3c', linewidth=2, label=f'ROC (AUC = {roc_auc:.3f})')

# Рисуем диагональ — это "кривая" случайного угадывания (AUC = 0.5).
# 'k--' — чёрная пунктирная линия.
ax.plot([0, 1], [0, 1], 'k--', alpha=0.5, label='Random (AUC = 0.500)')

# Закрашиваем область под ROC-кривой для наглядности.
ax.fill_between(fpr, tpr, alpha=0.1, color='#e74c3c')
ax.set_xlabel('False Positive Rate')
ax.set_ylabel('True Positive Rate')
ax.set_title('ROC-кривая')
ax.legend()
ax.grid(True, alpha=0.3)

plt.tight_layout()
plt.savefig('img/roc_curve.png', dpi=150, bbox_inches='tight')
print(" ROC-кривая сохранена в img/roc_curve.png")

