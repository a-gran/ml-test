# ЧТО МЫ ДЕЛАЕМ?
# У нас есть таблица с данными о пациентах (возраст, давление, холестерин и т.д.)
# Мы хотим научить нейросеть угадывать: болен пациент сердечной
# болезнью или нет. Это называется "бинарная классификация" — два варианта:
# 1 = болен, 0 = здоров.
# =============================================================================
# ПЛАН РАБОТЫ:
# 1) Загружаем данные и смотрим, что там внутри (EDA — Exploratory Data Analysis)
# 2) Готовим данные: превращаем текст в числа, нормализуем
# 3) Строим нейросеть (модель) на библиотеке Keras
# 4) Обучаем модель и проверяем, насколько хорошо она угадывает
# =============================================================================

# pandas — библиотека для работы с таблицами (как Excel, только в Python).
import pandas as pd

# numpy — библиотека для математических операций с числами и массивами.
import numpy as np

# matplotlib — библиотека для создания графиков (диаграммы, гистограммы и пр.)
import matplotlib

# Эта строка говорит matplotlib: "не показывай окно с графиком на экране,
# а просто сохраняй картинку в файл". Используется, когда нет монитора (например, на сервере).
matplotlib.use('Agg')

# Подключаем модуль pyplot из matplotlib — именно он умеет рисовать графики.
import matplotlib.pyplot as plt

# seaborn — ещё одна библиотека для красивых графиков, надстройка над matplotlib.
# Умеет рисовать тепловые карты (heatmaps) и другие полезные визуализации.
import seaborn as sns

# Из библиотеки scikit-learn (sklearn) импортируем инструменты:
# train_test_split — функция, которая делит данные на две части:
# одна для обучения модели (train), другая для проверки (test).
# Это как подготовка к экзамену: учишься по одним задачам, сдаёшь по другим.
from sklearn.model_selection import train_test_split

# StandardScaler — инструмент для нормализации чисел.
# Он приводит все числа к одному масштабу (среднее = 0, разброс = 1).
# Зачем? Если возраст = 50, а давление = 130, нейросеть может "подумать",
# что давление важнее просто потому, что число больше. Нормализация это исправляет.

# LabelEncoder — превращает текстовые значения в числа.
# Например: "Male" → 0, "Female" → 1. Нейросети не понимают текст, только числа.
from sklearn.preprocessing import StandardScaler, LabelEncoder

# Импортируем метрики — инструменты для оценки качества модели:
# classification_report — подробный отчёт: precision, recall, f1-score для каждого класса.
#   - Precision (точность): из всех, кого модель назвала "больным", сколько реально больны?
#   - Recall (полнота): из всех реально больных, скольких модель нашла?
#   - F1-score: среднее между precision и recall (одно число вместо двух).
# confusion_matrix — матрица ошибок. Таблица 2×2, показывающая:
#   - сколько здоровых правильно определены как здоровые (True Negative)
#   - сколько больных правильно определены как больные (True Positive)
#   - сколько здоровых ошибочно названы больными (False Positive)
#   - сколько больных ошибочно названы здоровыми (False Negative)
# roc_auc_score — метрика ROC-AUC. Число от 0 до 1.
# 1.0 = идеальная модель, 0.5 = случайное угадывание (как монетка).
# roc_curve — данные для построения ROC-кривой (графика качества модели).
# f1_score — вычисляет F1-метрику (баланс между precision и recall).
# accuracy_score — самая простая метрика: процент правильных ответов.
# Например, accuracy = 0.85 означает, что модель угадала 85% из всех случаев.
from sklearn.metrics import (classification_report, confusion_matrix, roc_auc_score, roc_curve, f1_score, accuracy_score)

# tensorflow — основная библиотека для создания нейросетей от Google.
# Это как "движок", на котором работает нейросеть.
import tensorflow as tf

# keras — высокоуровневый интерфейс (обёртка) для tensorflow.
# Он делает создание нейросетей простым и понятным, как конструктор LEGO.
from tensorflow import keras

# layers — это "кирпичики" нейросети: слои нейронов.
# callbacks — это "помощники", которые следят за обучением и могут его остановить
# или изменить параметры по ходу дела.
from tensorflow.keras import layers, callbacks, regularizers

# warnings — стандартный модуль Python для управления предупреждениями.
import warnings

# Говорим Python: "не показывай мне предупреждения, они мешают читать вывод".
# Предупреждения — это не ошибки, программа работает, но Python хочет нас о чём-то
# предупредить (например, что какая-то функция скоро устареет).
warnings.filterwarnings('ignore')

# ─────────────────────────────────────────────────────────────────────────────
# ФИКСИРУЕМ СЛУЧАЙНОСТЬ (SEED)
# ─────────────────────────────────────────────────────────────────────────────
# Многие операции в машинном обучении используют случайные числа
# (например, начальные веса нейросети, перемешивание данных).
# Если не зафиксировать "зерно" (seed), каждый запуск будет давать разные результаты.
# seed=42 — это просто число-"пароль". Можно поставить любое число.
# Главное — при одном и том же seed результаты всегда будут одинаковыми.
# Это нужно для воспроизводимости: чтобы ты и я получили одинаковые результаты.

np.random.seed(42)       # Фиксируем случайность для numpy
tf.random.set_seed(42)   # Фиксируем случайность для tensorflow

# =============================================================================
# ЧАСТЬ 1: ЗАГРУЗКА И БАЗОВЫЙ АНАЛИЗ ДАННЫХ (EDA)
# =============================================================================
# EDA = Exploratory Data Analysis = Исследовательский анализ данных.
# Это этап, когда мы просто СМОТРИМ на данные: какие столбцы, какие значения,
# есть ли пропуски, как распределены классы. Без этого нельзя — нужно понимать,
# с чем мы работаем, прежде чем учить модель.
# =============================================================================

# Печатаем красивый заголовок в консоли.
# "=" * 70 — это строка из 70 символов "=", просто для красоты.
print("=" * 70)
print("ЧАСТЬ 1: ЗАГРУЗКА И БАЗОВЫЙ АНАЛИЗ ДАННЫХ (EDA)")
print("=" * 70)

# ─────────────────────────────────────────────────────────────────────────────
# 1.1 Загрузка данных из CSV-файла
# ─────────────────────────────────────────────────────────────────────────────
# CSV (Comma-Separated Values) — это текстовый файл, где данные разделены запятыми.
# Выглядит как таблица. pd.read_csv() читает этот файл и превращает его в DataFrame —
# это объект pandas, по сути таблица с именованными столбцами и строками.
# Результат сохраняем в переменную df (сокращение от DataFrame).
df = pd.read_csv('heart.csv')

# df.shape — это пара (количество_строк, количество_столбцов).
# df.shape[0] — строки (пациенты), df.shape[1] — столбцы (признаки + целевая переменная).
# f-строка (f"...") позволяет подставлять значения переменных прямо в текст через {}.
print(f"\nРазмер датасета: {df.shape[0]} строк × {df.shape[1]} столбцов")

# df.dtypes — показывает тип данных каждого столбца.
# int64 = целое число, float64 = число с дробной частью, object = текст (строка).
print(f"\nСтолбцы и типы данных:")
print(df.dtypes)

# df.head() — показывает первые 5 строк таблицы.
# Это быстрый способ посмотреть, как выглядят данные.
print(f"\nПервые 5 строк:")
print(df.head())

# df.describe() — считает статистику по каждому числовому столбцу:
# count (сколько значений), mean (среднее), std (стандартное отклонение = разброс),
# min, max, 25%/50%/75% (квартили — значения, ниже которых лежит 25/50/75% данных).
# .round(2) — округляем до 2 знаков после запятой, чтобы было читаемо.
print(f"\nОписательная статистика (числовые признаки):")
print(df.describe().round(2))

# ─────────────────────────────────────────────────────────────────────────────
# 1.2 Проверка на пропуски и дубликаты
# ─────────────────────────────────────────────────────────────────────────────
# Пропуски (NaN, null) — это пустые ячейки в таблице. Если в столбце "Возраст"
# у какого-то пациента нет значения — это пропуск. Нейросеть не умеет работать
# с пропусками, поэтому важно проверить.
# df.isnull() — создаёт таблицу из True/False: True там, где пропуск.
# .sum() — считает количество True (пропусков) в каждом столбце.
print(f"\nПропущенные значения:")
print(df.isnull().sum())

# .sum().sum() — суммирует ещё раз, чтобы получить ОБЩЕЕ число пропусков во всей таблице.
print(f"\nВсего пропусков: {df.isnull().sum().sum()}")

# df.duplicated() — проверяет, есть ли полностью одинаковые строки (дубликаты).
# .sum() — считает их количество.
# Дубликаты могут исказить обучение: модель будет "переучиваться" на повторяющихся данных.
duplicates = df.duplicated().sum()
print(f"\nДубликаты: {duplicates}")

# ─────────────────────────────────────────────────────────────────────────────
# 1.3 Анализ целевой переменной (баланс классов)
# ─────────────────────────────────────────────────────────────────────────────
# Целевая переменная (target) — это то, что мы хотим предсказать.
# В нашем случае: HeartDisease (1 = болезнь, 0 = норма).
# Баланс классов — это соотношение между классами.
# Если 90% данных — "норма" и только 10% — "болезнь", это ДИСБАЛАНС.
# Модель может просто всем говорить "норма" и получить accuracy 90%,
# при этом вообще не находя больных людей. Это проблема.
print(f"\nРаспределение целевой переменной (HeartDisease):")

# .value_counts() — считает, сколько раз встречается каждое значение.
# Результат: сколько строк с 0 (норма) и сколько с 1 (болезнь).
target_counts = df['HeartDisease'].value_counts()
print(target_counts)

# Считаем процент каждого класса.
# target_counts[1] — количество больных, len(df) — всего строк.
# :.1f — формат: одна цифра после запятой.
print(f"\nДоля класса 1 (болезнь): {target_counts[1]/len(df)*100:.1f}%")
print(f"Доля класса 0 (норма):   {target_counts[0]/len(df)*100:.1f}%")

# Считаем отношение меньшего класса к большему.
# Если ratio > 0.8, классы примерно равны — всё хорошо.
# Если ratio < 0.5, один класс сильно больше другого — нужны специальные методы.
balance_ratio = target_counts.min() / target_counts.max()

if balance_ratio > 0.8:
    print("Классы хорошо сбалансированы")
elif balance_ratio > 0.5:
    print("Умеренный дисбаланс классов")
else:
    print("Сильный дисбаланс классов — нужны специальные меры")

# ─────────────────────────────────────────────────────────────────────────────
# 1.4 Анализ категориальных и числовых признаков
# ─────────────────────────────────────────────────────────────────────────────
# Признаки (features) бывают двух типов:
# 1) КАТЕГОРИАЛЬНЫЕ — это текстовые метки, которые обозначают группу/категорию.
#    Пример: Пол ("M"/"F"), Тип боли ("ASY", "NAP", "ATA", "TA").
#    Их НЕЛЬЗЯ напрямую подать в нейросеть — нужно сначала превратить в числа.
# 2) ЧИСЛОВЫЕ — это уже числа: возраст (54), давление (130), холестерин (250) и т.д.
#    Их можно подавать в нейросеть, но желательно сначала нормализовать.

# df.select_dtypes(include='object') — выбирает столбцы, где тип данных = текст (object).
# .columns.tolist() — превращает названия столбцов в обычный список Python.
cat_cols = df.select_dtypes(include='object').columns.tolist()

# Аналогично для числовых столбцов (int64 = целые числа, float64 = дробные числа).
num_cols = df.select_dtypes(include=['int64', 'float64']).columns.tolist()

# Удаляем 'HeartDisease' из списка числовых признаков, потому что это
# не признак, а ЦЕЛЕВАЯ переменная (то, что мы предсказываем).
# Мы не хотим, чтобы модель "подсматривала" правильный ответ.
num_cols.remove('HeartDisease')

print(f"\nКатегориальные признаки ({len(cat_cols)}): {cat_cols}")
print(f"Числовые признаки ({len(num_cols)}): {num_cols}")

# Для каждого категориального столбца выводим, какие значения там есть
# и сколько раз каждое встречается.
# Это помогает понять структуру данных.
for col in cat_cols:
    print(f"\n  {col}:")
    # .value_counts() считает частоту каждого значения.
    # .to_dict() превращает результат в словарь для компактного вывода.
    print(f"{df[col].value_counts().to_dict()}")

# ─────────────────────────────────────────────────────────────────────────────
# 1.5 Визуализация EDA (рисуем графики)
# ─────────────────────────────────────────────────────────────────────────────
# Графики помогают УВИДЕТЬ закономерности, которые сложно заметить в числах.
# Мы создаём "сетку" из 6 графиков (2 строки × 3 столбца).
# plt.subplots(2, 3, ...) — создаёт фигуру (холст) и 6 "осей" (axes) для графиков.
# figsize=(16, 10) — размер холста в дюймах (ширина × высота).
fig, axes = plt.subplots(2, 3, figsize=(16, 10))

# suptitle — главный заголовок всей фигуры (над всеми графиками).
fig.suptitle('Exploratory Data Analysis — Heart Failure Prediction', fontsize=16, fontweight='bold')

# --- График 1: Баланс классов (столбчатая диаграмма) ---
# Показывает, сколько пациентов в каждом классе (здоровые vs больные).

# axes[0, 0] — первый график (строка 0, столбец 0).
ax = axes[0, 0]

# Два цвета: зелёный для здоровых, красный для больных.
colors = ['#2ecc71', '#e74c3c']

# .plot(kind='bar') — рисует столбчатую диаграмму.
# ax=ax — рисовать именно на нашей "оси" (в нашей ячейке сетки).
# edgecolor='black' — чёрная обводка столбцов.
target_counts.plot(kind='bar', ax=ax, color=colors, edgecolor='black')

# Подписи графика: заголовок, ось X, ось Y.
ax.set_title('Распределение классов')
ax.set_xlabel('HeartDisease')
ax.set_ylabel('Количество')

# Заменяем числовые подписи на оси X на понятные слова.
# rotation=0 — текст горизонтально (не повёрнут).
ax.set_xticklabels(['0 (Норма)', '1 (Болезнь)'], rotation=0)

# Добавляем число над каждым столбцом, чтобы точно видеть значение.
# enumerate() — пробегает по элементам с их порядковым номером (индексом).
# i — индекс (0 или 1), v — значение (количество пациентов).
for i, v in enumerate(target_counts):
    # ax.text(x, y, текст) — пишет текст в координатах (x, y) на графике.
    # ha='center' — выравнивание по центру.
    ax.text(i, v + 5, str(v), ha='center', fontweight='bold')

# --- График 2: Распределение возраста по классам (гистограмма) ---
# Гистограмма показывает, как распределены значения.
# Мы накладываем две гистограммы друг на друга: здоровые и больные.

ax = axes[0, 1]  # Вторая ячейка сетки (строка 0, столбец 1).

# df[df['HeartDisease'] == 0] — фильтрация: берём только строки, где HeartDisease = 0.
# ['Age'] — берём столбец "Возраст" из отфильтрованных данных.
# .hist() — рисует гистограмму.
# bins=20 — разделить данные на 20 корзинок (столбиков гистограммы).
# alpha=0.6 — прозрачность (0 = невидимо, 1 = непрозрачно). 0.6 = полупрозрачно, чтобы видеть обе гистограммы, даже если они накладываются.
# label — подпись для легенды графика.
df[df['HeartDisease'] == 0]['Age'].hist(ax=ax, bins=20, alpha=0.6, color='#2ecc71', label='Норма')
df[df['HeartDisease'] == 1]['Age'].hist(ax=ax, bins=20, alpha=0.6, color='#e74c3c', label='Болезнь')
ax.set_title('Распределение возраста')
ax.set_xlabel('Возраст')

# ax.legend() — показывает легенду (маленькую табличку с цветами и подписями).
ax.legend()

# --- График 3: Максимальная ЧСС по классам ---
# ЧСС = частота сердечных сокращений (пульс).
# MaxHR = максимальный пульс, достигнутый при нагрузке.
# У больных людей обычно ниже максимальный пульс.
ax = axes[0, 2]  # Третья ячейка (строка 0, столбец 2).
df[df['HeartDisease'] == 0]['MaxHR'].hist(ax=ax, bins=20, alpha=0.6, color='#2ecc71', label='Норма')
df[df['HeartDisease'] == 1]['MaxHR'].hist(ax=ax, bins=20, alpha=0.6, color='#e74c3c', label='Болезнь')
ax.set_title('Макс. ЧСС по классам')
ax.set_xlabel('MaxHR')
ax.legend()

# --- График 4: Тип боли в груди vs Болезнь ---
# ChestPainType может быть: ASY (бессимптомная), NAP, ATA, TA.
# Мы хотим увидеть: при каком типе боли чаще встречается болезнь?
ax = axes[1, 0]  # Четвёртая ячейка (строка 1, столбец 0).

# pd.crosstab() — создаёт таблицу сопряжённости (cross-tabulation).
# Считает, сколько пациентов с каждым типом боли попали в каждый класс.
# Пример: ASY-0: 150, ASY-1: 330 → у бессимптомных чаще болезнь!
ct = pd.crosstab(df['ChestPainType'], df['HeartDisease'])

# Рисуем столбчатую диаграмму по этой таблице.
ct.plot(kind='bar', ax=ax, color=colors, edgecolor='black')
ax.set_title('Тип боли в груди vs Болезнь')
ax.set_xlabel('ChestPainType')
ax.legend(['Норма', 'Болезнь'])

# Подписи на оси X горизонтально (rotation=0), чтобы было читаемо.
ax.set_xticklabels(ax.get_xticklabels(), rotation=0)

# --- График 5: Корреляционная матрица ---
# Корреляция — это мера связи между двумя числовыми признаками.
# Значение от -1 до +1:
#   +1 = идеальная прямая связь (один растёт → другой растёт)
#   -1 = идеальная обратная связь (один растёт → другой падает)
#    0 = связи нет
# Это помогает понять, какие признаки сильнее всего связаны с болезнью.
ax = axes[1, 1]  # Пятая ячейка (строка 1, столбец 1).
# Создаём копию таблицы, чтобы не испортить оригинал.
df_corr = df.copy()

# Категориальные столбцы (текст) нужно превратить в числа для расчёта корреляции.
# LabelEncoder() присваивает каждому уникальному тексту свой номер.
# .fit_transform() — "обучается" на данных и сразу преобразует их.
for col in cat_cols:
    df_corr[col] = LabelEncoder().fit_transform(df_corr[col])

