#                    Предсказано: Норма    Предсказано: Болезнь
# На самом деле Норма     TN                    FP
# На самом деле Болезнь   FN                    TP

# TN (True Negative)  — здоровый правильно определён как здоровый
# TP (True Positive)  — больной правильно определён как больной
# FP (False Positive) — здоровый ошибочно назван больным (ложная тревога)
